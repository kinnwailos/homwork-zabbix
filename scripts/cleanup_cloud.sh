#!/bin/bash
#===============================================================================
# ОЧИСТКА YANDEX CLOUD ПОСЛЕ ДЗ
# Удаляет все ресурсы, созданные в ходе лабораторной работы
#===============================================================================
# Полное развёртывание
# python3 deploy_zabbix.py

# Или с подробным выводом
# python3 deploy_zabbix.py --verbose

# Или сухой запуск (покажет команды без выполнения)
# python3 deploy_zabbix.py --dry-run
set -e  # Остановка при ошибке

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info()    { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[OK]${NC} $1"; }
log_warn()    { echo -e "${YELLOW}[WARN]${NC} $1"; }
log_error()   { echo -e "${RED}[ERROR]${NC} $1"; }

#-------------------------------------------------------------------------------
# ПРЕДУПРЕЖДЕНИЕ
#-------------------------------------------------------------------------------
echo ""
echo -e "${RED}===============================================================================${NC}"
echo -e "${RED}                    ⚠️  ВНИМАНИЕ: УДАЛЕНИЕ ВСЕХ РЕСУРСОВ ⚠️                  ${NC}"
echo -e "${RED}===============================================================================${NC}"
echo ""
echo "Будут удалены:"
echo "  • Виртуальные машины: zabbix-server, agent-1, agent-2"
echo "  • Снимки дисков (snapshots)"
echo "  • Группа безопасности: zabbix-sg"
echo "  • Подсеть: zabbix-subnet"
echo "  • Сеть: zabbix-network"
echo ""
echo -e "${YELLOW}Все данные будут потеряны безвозвратно!${NC}"
echo ""
read -p "Вы уверены? Введите 'YES' для продолжения: " confirm

if [ "$confirm" != "YES" ]; then
    log_error "Операция отменена пользователем"
    exit 1
fi

echo ""
log_info "Начало очистки..."
echo ""

#-------------------------------------------------------------------------------
# 1. УДАЛЕНИЕ ВИРТУАЛЬНЫХ МАШИН
#-------------------------------------------------------------------------------
log_info "Удаление виртуальных машин..."

for vm in zabbix-server agent-1 agent-2; do
    if yc compute instance get $vm &> /dev/null; then
        log_info "Удаление ВМ: $vm"
        yc compute instance delete $vm --async
        log_success "ВМ $vm удалена"
    else
        log_warn "ВМ $vm не найдена (пропущено)"
    fi
done

# Ждём удаления ВМ
log_info "Ожидание удаления ВМ (30 секунд)..."
sleep 30

#-------------------------------------------------------------------------------
# 2. УДАЛЕНИЕ СНЯБКОВ (SNAPSHOTS)
#-------------------------------------------------------------------------------
log_info "Удаление снимков дисков..."

for snapshot in $(yc compute snapshot list --format json | jq -r '.[].name' | grep -E "zabbix|agent"); do
    log_info "Удаление снимка: $snapshot"
    yc compute snapshot delete $snapshot
    log_success "Снимок $snapshot удалён"
done

#-------------------------------------------------------------------------------
# 3. УДАЛЕНИЕ ГРУППЫ БЕЗОПАСНОСТИ
#-------------------------------------------------------------------------------
log_info "Удаление группы безопасности..."

if yc vpc security-group get zabbix-sg &> /dev/null; then
    yc vpc security-group delete zabbix-sg
    log_success "Группа безопасности zabbix-sg удалена"
else
    log_warn "Группа безопасности не найдена (пропущено)"
fi

#-------------------------------------------------------------------------------
# 4. УДАЛЕНИЕ ПОДСЕТИ
#-------------------------------------------------------------------------------
log_info "Удаление подсети..."

if yc vpc subnet get zabbix-subnet &> /dev/null; then
    yc vpc subnet delete zabbix-subnet
    log_success "Подсеть zabbix-subnet удалена"
else
    log_warn "Подсеть не найдена (пропущено)"
fi

#-------------------------------------------------------------------------------
# 5. УДАЛЕНИЕ СЕТИ
#-------------------------------------------------------------------------------
log_info "Удаление сети..."

if yc vpc network get zabbix-network &> /dev/null; then
    yc vpc network delete zabbix-network
    log_success "Сеть zabbix-network удалена"
else
    log_warn "Сеть не найдена (пропущено)"
fi

#-------------------------------------------------------------------------------
# 6. ПРОВЕРКА ОЧИСТКИ
#-------------------------------------------------------------------------------
echo ""
log_info "Проверка остаточных ресурсов..."
echo ""

echo "=== Виртуальные машины ==="
yc compute instance list --format json | jq -r '.[].name' | grep -E "zabbix|agent" || echo "Нет ресурсов"

echo ""
echo "=== Снимки дисков ==="
yc compute snapshot list --format json | jq -r '.[].name' | grep -E "zabbix|agent" || echo "Нет ресурсов"

echo ""
echo "=== Сети ==="
yc vpc network list --format json | jq -r '.[].name' | grep "zabbix" || echo "Нет ресурсов"

echo ""
echo -e "${GREEN}===============================================================================${NC}"
echo -e "${GREEN}                    ✅ ОЧИСТКА ЗАВЕРШЕНА УСПЕШНО! ✅                         ${NC}"
echo -e "${GREEN}===============================================================================${NC}"
echo ""
log_success "Все ресурсы Zabbix удалены из Yandex Cloud"
log_info "Теперь вы не будете платить за неиспользуемые ВМ"
echo ""
log_info "Локальные файлы сохранены в: ~/zabbix-project/"
echo ""